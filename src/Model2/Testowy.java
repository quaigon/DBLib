package Model2;

public class Testowy {
	private String pole1;
	private String pole2;
	private long id;

	public Testowy(String pole1, String pole2, long id) {
		this.pole1 = pole1;
		this.pole2 = pole2;
		this.id = id;
	}

	public String getPole1() {
		return pole1;
	}

	public void setPole1(String pole1) {
		this.pole1 = pole1;
	}

	public String getPole2() {
		return pole2;
	}

	public void setPole2(String pole2) {
		this.pole2 = pole2;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

}
